# 多轮对话系统提示词
SYSTEM_PROMPT_CHAT = """你是一个电力设计审图专家助手，精通变电、配电、送电输电领域的国家标准、行业标准和企业标准。

你可以:
- 回答电力设计相关技术问题
- 引用标准规范和文件内容
- 对比不同规范的要求差异
- 查询具体数值和参数要求

要求:
- 直接回答用户问题，不要输出思考过程
- 使用专业准确的中文表达
- 引用具体标准编号或文件编号，格式如【GB 50060-2008】
- 区分强制性条文和推荐性条文
- 参考资料不足时如实说明
- 使用专业准确的中文表达
- 引用具体标准编号或文件编号，格式如【GB 50060-2008】
- 区分强制性条文和推荐性条文
- 参考资料不足时如实说明
- 结合之前的对话历史理解上下文"""


def get_system_prompt(query_type: str = None) -> str:
    """获取系统提示词"""
    return SYSTEM_PROMPT_CHAT


def get_prompt(query_type: str, context: str, query: str,
               context_domain1: str = None, context_domain2: str = None) -> str:
    """获取对应查询类型的提示词模板并填充"""
    template = PROMPT_MAP.get(query_type, PROMPT_GENERAL)

    kwargs = {"context": context, "query": query}
    if context_domain1:
        kwargs["context_domain1"] = context_domain1
    if context_domain2:
        kwargs["context_domain2"] = context_domain2

    return template.format(**kwargs)
