def load_existing_sample(sample_path: str) -> list:
    """加载已有 sample_eval_dataset.json 并转换为统一格式。
    key_facts 和 expected_citations 已内嵌在 JSON 中，无需额外扩展。
    """
    if not os.path.exists(sample_path):
        print(f"[WARN] 示例数据集不存在: {sample_path}，跳过")
        return []

    with open(sample_path, encoding="utf-8") as f:
        sample = json.load(f)

    # 数据集文件名 → 实际库中文件名 映射表
    SRC_TO_ACTUAL = {
        "GB50052-2009《供配电系统设计规范》.pdf":
            "G11_GB50052-2009_供配电系统设计规范.pdf",
        "GB50065-2011《交流电气装置的接地设计规范》.pdf":
            "交流电气装置的接地设计规范.pdf",
        "GB 50217-2018 电力工程电缆设计标准.pdf":
            "GB_50217-2018_电力工程电缆设计标准.pdf",
    }

    converted = []
    for q in sample:
        qid = q.get("id", "")
        src_files = q.get("source_files", [])

        # 将数据集中的文件名映射为实际库中文件名
        actual_files = [SRC_TO_ACTUAL.get(f, f) for f in src_files]
        actual_top1 = SRC_TO_ACTUAL.get(src_files[0], src_files[0]) if src_files else ""

        new_q = {
            "id": qid,
            "question": q.get("question", ""),
            "answer": q.get("answer", ""),
            "gold_answer": q.get("answer", ""),
            "source_files": actual_files,
            "source_sections": q.get("source_sections", []),
            "domain": q.get("domain", ""),
            "category": q.get("category", ""),
            "question_type": q.get("question_type", ""),
            "difficulty": q.get("difficulty", "medium"),
            "keywords": q.get("keywords", []),
            "expected_chunks": q.get("expected_chunks", 2),
            "evaluation_dimensions": q.get("evaluation_dimensions", {}),
            # eval 框架字段 — 直接从 sample 读取（已内嵌）
            "domain_filter": q.get("domain", ""),
            "expected_keywords": q.get("keywords", []),
            "expected_top1_doc": actual_top1,
            "key_facts": q.get("key_facts", []),
            "expected_citations": q.get("expected_citations", []),
        }
        converted.append(new_q)
    return converted


def build():
    """生成最终数据集 JSON — 合并已有20题 + 新增49题"""
    sample_path = os.path.join(PROJECT_ROOT, "eval", "datasets", "sample_eval_dataset.json")
    existing = load_existing_sample(sample_path)

    all_questions = existing + QUESTIONS

    # 统一: 对仅有 answer 的问题补充 gold_answer
    for q in all_questions:
        if "gold_answer" not in q and "answer" in q:
            q["gold_answer"] = q["answer"]

    # 重新编号: EVAL-001, EVAL-002, ... 确保连续无空缺
    for i, q in enumerate(all_questions, 1):
        q["id"] = f"EVAL-{i:03d}"

    # 更新域计数
    dm_count = {}
    for q in all_questions:
        dm = q.get("domain", "?")
        dm_count[dm] = dm_count.get(dm, 0) + 1

    dataset = {
        "dataset_name": "榕能电力审图知识库 全维度RAG评估数据集 v3.0",
        "description": f"覆盖全202个入库文件(配电121/变电63/送电输电18)，含检索/召回/答案质量三维评估。已有20题(样本集)+新增{len(QUESTIONS)}题={len(all_questions)}题",
        "total_files_covered": 202,
        "domain_distribution": {
            "配电": {"file_count": 121, "question_count": dm_count.get("配电", 0)},
            "变电": {"file_count": 63, "question_count": dm_count.get("变电", 0)},
            "送电输电": {"file_count": 18, "question_count": dm_count.get("送电输电", 0)},
        },
        "version": "3.0",
        "created": "2026-06-25",
        "total_questions": len(all_questions),
        "questions": all_questions,
    }

    output_path = os.path.join(PROJECT_ROOT, "eval", "datasets", "comprehensive_eval_dataset.json")
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(dataset, f, ensure_ascii=False, indent=2)

    # Statistics
    domains = {}
    qtypes = {}
    difficulties = {}
    for q in all_questions:
        dm = q.get("domain", "?")
        domains[dm] = domains.get(dm, 0) + 1
        qt = q.get("question_type", "?")
        qtypes[qt] = qtypes.get(qt, 0) + 1
        df = q.get("difficulty", "easy")
        difficulties[df] = difficulties.get(df, 0) + 1

    print(f"[OK] 完整评估数据集已生成: {output_path}")
    print(f"    已有样本: {len(existing)} 题")
    print(f"    本次新增: {len(QUESTIONS)} 题")
    print(f"    ---------")
    print(f"    总题目数: {len(all_questions)}")
    print(f"\n按域分布:")
    for k in ["配电", "变电", "送电输电"]:
        v = domains.get(k, 0)
        pct = v / len(all_questions) * 100
        print(f"    {k}: {v} 题 ({pct:.0f}%)")
    print(f"\n按题型分布:")
    for k, v in sorted(qtypes.items(), key=lambda x: -x[1]):
        print(f"    {k}: {v} 题")
    print(f"\n按难度分布:")
    for k in ["easy", "medium", "hard"]:
        v = difficulties.get(k, 0)
        print(f"    {k}: {v} 题")


if __name__ == "__main__":
    build()
